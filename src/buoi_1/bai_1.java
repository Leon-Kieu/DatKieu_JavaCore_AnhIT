package buoi_1;

public class bai_1 {
    // Viết chương trình khai báo các biến sau và in chúng ra màn hình
    //Biến name kiểu String có giá trị là tên của bạn.
    //Biến age kiểu int có giá trị là tuổi của bạn.
    //Biến height kiểu double có giá trị là chiều cao của bạn.
    //Biến isStudent kiểu boolean với giá trị true hoặc false.

    public static void main (String[] args){
        // Khai báo biến
        String name = "Nguyen Van A";
        int tuoi = 20;
        double chieuCao = 1.69;
        boolean isStudent = true;
        // In ra màn hình
        System.out.println("Tên: "+ name);
        System.out.println("Tuổi: "+ tuoi);
        System.out.println("Chiều cao: "+ chieuCao);
        System.out.println("Là sinh viên: "+ isStudent);
    }
}
