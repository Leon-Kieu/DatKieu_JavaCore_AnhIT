package buoi_4;

public class duyet_mang_for {

    public static void main(String[] args){
        int[] numbers = {10, 20, 30, 25, 40, 50};
        System.out.println("Duyệt mảng bằng for");


        for (int i =0; i < numbers.length; i++){
            System.out.println("Phần tử tại index thứ: " +i +" cua mảng có giá trị là: " +numbers[i]);
        }
    }






}
